// Write an if statement that gives users a score (A, B, C, D, or F)
//   based on the test results

var result = 92;

if( result >= 90 ) {
  console.log( "Score: A" );
}
else if( result >= 80 ) {
  console.log( "Score: B" );
}
else if( result >= 70 ) {
  console.log( "Score: C" );
}
else if( result >= 60 ) {
  console.log( "Score: D" );
}
else {
  console.log( "Score: F" );
}
